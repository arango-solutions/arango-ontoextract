"""Driver to run every ops benchmark and write a baseline file.

Usage:

    python -m benchmarks.operations.run_baselines

Or via Make:

    make bench

Writes results to ``benchmarks/operations/baseline.md`` if invoked
with ``--update-baseline``; otherwise just prints the rendered
tables to stdout. The default (print, don't write) makes it safe
to run in CI as a smoke check that benchmarks haven't started
raising; ``--update-baseline`` is the explicit "I'm refreshing
the committed numbers" gesture.

Recording protocol:
1. Run on a quiet host (close other apps; close Docker GUI if
   memory pressure is high).
2. Capture the host snapshot (kernel, CPU, RAM, Python version)
   in the file header -- baseline numbers are meaningless without
   this context.
3. Commit the result; reviewers can then compare against the
   per-PR run in the description.
"""

from __future__ import annotations

import argparse
import os
import platform
import sys
from datetime import UTC, datetime
from pathlib import Path

# Add the project root + backend to sys.path so this script can
# run as ``python -m benchmarks.operations.run_baselines`` from
# the repo root without needing an editable install.
_REPO_ROOT = Path(__file__).resolve().parents[2]
_BACKEND = _REPO_ROOT / "backend"
if str(_BACKEND) not in sys.path:
    sys.path.insert(0, str(_BACKEND))

os.environ.setdefault("ANTHROPIC_API_KEY", "bench-noop")
os.environ.setdefault("OPENAI_API_KEY", "bench-noop")
os.environ.setdefault("ARANGO_PASSWORD", "bench-noop")
os.environ.setdefault("JWT_SECRET_KEY", "bench-noop-secret-key-32-bytes!!")
os.environ.setdefault("APP_LOG_LEVEL", "ERROR")

from benchmarks.operations import (  # noqa: E402
    bench_api_latency,
    bench_materialize,
    bench_temporal_snapshot,
)
from benchmarks.operations.harness import print_results_table  # noqa: E402

BASELINE_PATH = _REPO_ROOT / "benchmarks" / "operations" / "baseline.md"


def _host_snapshot() -> str:
    """Capture the host info that gives the numbers their context.

    Markdown so it drops cleanly into ``baseline.md`` -- the
    reviewer sees CPU / RAM / Python version next to the
    measurements and can decide whether their machine should
    produce comparable numbers.
    """
    return "\n".join(
        [
            "| Field | Value |",
            "| --- | --- |",
            f"| Date (UTC) | {datetime.now(UTC).isoformat(timespec='seconds')} |",
            f"| OS | {platform.system()} {platform.release()} |",
            f"| Machine | {platform.machine()} |",
            f"| Python | {platform.python_version()} |",
            f"| Processor | {platform.processor() or 'unknown'} |",
        ]
    )


def main(*, update_baseline: bool) -> int:
    """Run every benchmark, render the report, optionally write it.

    Returns 0 on success; the harness raises if any benchmark
    fails so this function never has to deal with partial state.
    """
    sections = [
        ("API Latency", bench_api_latency.run_all()),
        ("Materialization Throughput", bench_materialize.run_all()),
        ("Temporal Snapshot", bench_temporal_snapshot.run_all()),
    ]

    parts: list[str] = []
    parts.append("# Ops Benchmark Baselines")
    parts.append("")
    parts.append(
        "> Generated by ``benchmarks/operations/run_baselines.py``. See "
        "``benchmarks/operations/README.md`` for what each scenario "
        "measures and how to interpret the numbers."
    )
    parts.append("")
    parts.append("## Host")
    parts.append("")
    parts.append(_host_snapshot())
    parts.append("")

    for title, results in sections:
        parts.append(f"## {title}")
        parts.append("")
        parts.append(print_results_table(results))
        parts.append("")

    report = "\n".join(parts)
    print(report)

    if update_baseline:
        BASELINE_PATH.write_text(report + "\n")
        # ``relative_to`` raises if BASELINE_PATH lives outside the
        # repo (eg a tmp dir in a test); fall back to the absolute
        # path so the log line is always usable.
        try:
            display = BASELINE_PATH.relative_to(_REPO_ROOT)
        except ValueError:
            display = BASELINE_PATH
        print(f"\n[wrote {display}]", file=sys.stderr)

    return 0


if __name__ == "__main__":  # pragma: no cover -- script entry point
    parser = argparse.ArgumentParser(
        description="Run the AOE ops benchmark suite and render results."
    )
    parser.add_argument(
        "--update-baseline",
        action="store_true",
        help=(
            "Overwrite ``benchmarks/operations/baseline.md`` with the new "
            "results. Default behaviour is to print only -- safer for CI "
            "smoke checks."
        ),
    )
    args = parser.parse_args()
    sys.exit(main(update_baseline=args.update_baseline))
